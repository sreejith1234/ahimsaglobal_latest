$(function() {
  let storedLang;
  const api_key = "at_0BdAWoLT79lZbivJddgUAXaIXIWYm";
  fetch(`https://api.ipify.org?format=json`).then((response) => {
    return response.json();
  }).then((json) => {
    const ip = json.ip;
    $.ajax({
      url: "https://geo.ipify.org/api/v1",
      data: {apiKey: api_key, ipAddress: ip},
      success: function(data) {
        console.log(data);
        const country = data.location.country;
        switch (country) {
          case "IN":
            storedLang = 'en';
            $('#changeLangBtn').hide();
            $('#returnLangBtn').hide();
            break;
          case "CA":
            storedLang = 'sp';
            $('#changeLangBtn').show();
            $('#returnLangBtn').hide();
            break;
          default:
            storedLang = localStorage.getItem('stored_lang') || 'en';
            if (storedLang === 'sp') {
              $('#changeLangBtn').show();
              $('#returnLangBtn').hide();
            } else {
              $('#changeLangBtn').hide();
              $('#returnLangBtn').hide();
            }
        }
        console.log('Country:', country);
        console.log('Stored language:', storedLang);
        localStorage.setItem('stored_lang', storedLang);
        translateLang(storedLang);
      }
    });
  });

  $('#changeLangBtn').on('click', function() {
    storedLang = 'en';
    localStorage.setItem('stored_lang', storedLang);
    translateLang(storedLang);
    $('#changeLangBtn').hide();
    $('#returnLangBtn').show();
  });

  $('#returnLangBtn').on('click', function() {
    storedLang = 'sp';
    localStorage.setItem('stored_lang', storedLang);
    translateLang(storedLang);
    $('#changeLangBtn').show();
    $('#returnLangBtn').hide();
  });

  function translateLang(lang = storedLang) {
    $('.lang').each(function() {
      $(this).text(arrLang[lang][$(this).attr('key')]);
    });
  }
});

